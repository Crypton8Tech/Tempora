CURRENCY_CONFIG = {
    "rub": {"symbol": "₽", "rate": 1.0, "after": True},
    "usd": {"symbol": "$", "rate": 0.011, "after": False},
    "eur": {"symbol": "€", "rate": 0.010, "after": False},
}


def t(key: str, lang: str = "ru") -> str:
    return TRANSLATIONS.get(lang, TRANSLATIONS["ru"]).get(key, key)


def format_price(price_rub: float, currency: str = "rub") -> str:
    cfg = CURRENCY_CONFIG.get(currency, CURRENCY_CONFIG["rub"])
    converted = price_rub * cfg["rate"]
    formatted = f"{converted:,.0f}".replace(",", " ")
    if cfg["after"]:
        return f"{formatted} {cfg['symbol']}"
    return f"{cfg['symbol']}{formatted}"
