def loc(obj, field: str, lang: str) -> str:
    """Return localised field value. E.g. loc(product, 'name', 'en') checks name_en first."""
    if lang != "ru":
        val = getattr(obj, f"{field}_{lang}", None)
        if val:
            return val
    return getattr(obj, field, "")


