def t(key: str, lang: str = "en") -> str:
    return TRANSLATIONS.get(lang, TRANSLATIONS["en"]).get(key, key)


def format_price(price_eur: float, currency: str = "eur") -> str:
    cfg = CURRENCY_CONFIG.get(currency, CURRENCY_CONFIG["eur"])
    converted = price_eur * cfg["rate"]
    formatted = f"{converted:,.0f}".replace(",", "\u202f")  # narrow no-break space
    if cfg["after"]:
        return f"{formatted} {cfg['symbol']}"
    return f"{cfg['symbol']}{formatted}"
